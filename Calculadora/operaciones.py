
def suma(valor1,valor2):
    resultado = valor1 + valor2
    return resultado

def resta(valor1,valor2):
    resultado = valor1 - valor2
    return resultado

def multi(valor1,valor2):
    resultado = valor1 * valor2
    return resultado

def division(valor1,valor2):
    resultado = valor1 / valor2
    return resultado

def potencia(valor1,valor2,):
    resultado = 1
    for i in range(1,(valor2+1)):
        resultado*=valor1
    return resultado

